public class AddingTwoNumbers {
    public static void main(Project5[] args) {
        int a=100; //variable a declaration and assignment
        int b=200;
        System.out.println("sum of the number" + (a+b)); //Adding two numbers
        System.out.println(a-b); //Subtracting of two numbers
        System.out.println(a*b); //Multiplication of two numbers
        System.out.println(a/b); //division of two numbers
    }
}

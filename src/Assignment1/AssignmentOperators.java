package Assignment1;
public class AssignmentOperators {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        a=b;
        System.out.println(a);
        System.out.println(a+=b);
        System.out.println(a=+b);
        System.out.println(a-=b);
        System.out.println(a=-b);
        System.out.println(a%=b);
        System.out.println(a*=b);
        System.out.println(a/=b);

    }
}

package Assignment1;

public class UnaryOperators {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
        System.out.println(++a);//a=1+10  //Pre Implement
        System.out.println(a++); // 10  //Post Implement
        System.out.println(a);
        System.out.println(a--);
        System.out.println(--a);
    }
}